var i1 = document.getElementById("i1");
var i12 = document.getElementById("i12");

function imgdown1() {	
	i1.style.transform="scale(0.9)";
	i12.style.transform="scale(0.9)";

	setTimeout(() => {
		i1.style.transform="scale(1)";
		i12.style.transform="scale(1)";
	}, 500);
}
function imgup1() {
		i1.style.transform="scale(1)";
		i12.style.transform="scale(1)";
}


