var links1 = document.getElementById( "a1" );

  links1.addEventListener( "touchstart", function() {
  	document.documentElement.style.setProperty('--txt1', "#2dcac2")
  	document.documentElement.style.setProperty('--bck1', "#2d2d2d")
  	document.documentElement.style.setProperty('--bdrbck1', "#2d2d2d")
  	document.documentElement.style.setProperty('--bxshad1', "inset 0px 0px 0px rgba(45,202,194,0.75),inset 0px 0px 0px rgba(0,0,0,1)")

	setTimeout(() => {
  	document.documentElement.style.setProperty('--txt1', "#fff")
  	document.documentElement.style.setProperty('--bck1', "#1f1f1f")
  	document.documentElement.style.setProperty('--bdrbck1', "#1f1f1f")
  	document.documentElement.style.setProperty('--bxshad1', "-2px -2px 3px rgba(45,202,194), 3px 3px 6px rgba(0,0,0,1)")
	}, 500);
  	});

  links1.addEventListener( "touchend", function() {
  	document.documentElement.style.setProperty('--txt1', "#fff")
  	document.documentElement.style.setProperty('--bck1', "#1f1f1f")
  	document.documentElement.style.setProperty('--bdrbck1', "#1f1f1f")
  	document.documentElement.style.setProperty('--bxshad1', "-2px -2px 3px rgba(45,202,194), 3px 3px 6px rgba(0,0,0,1)")
	});


