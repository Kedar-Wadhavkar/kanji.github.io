var links1 = document.getElementById( "a1" );

  links1.addEventListener( "touchstart", function() {
  	document.documentElement.style.setProperty('--txt1', "#fff")
  	document.documentElement.style.setProperty('--bck1', "rgba(255,255,255,0.2)")
  	document.documentElement.style.setProperty('--bdrbck1', "rgba(255,255,255,0.2)")

	setTimeout(() => {
  	document.documentElement.style.setProperty('--txt1', "#000")
  	document.documentElement.style.setProperty('--bck1', "rgba(255,255,255,0.9)")
  	document.documentElement.style.setProperty('--bdrbck1', "rgba(255,255,255,0.9)")
	}, 500);
  	});

  links1.addEventListener( "touchend", function() {
  	document.documentElement.style.setProperty('--txt1', "#000")
  	document.documentElement.style.setProperty('--bck1', "rgba(255,255,255,0.9)")
  	document.documentElement.style.setProperty('--bdrbck1', "rgba(255,255,255,0.9)")
	});


